#include<iostream>
using namespace std;
int main(){
    int n;
    cout <<"Enter the value of n: ";
    cin>>n;
    for(int i=1;i<=n;i++){
        for(int j=1;j<=i;j++){
            if(n<=5){
                cout<<" * ";
            }
            else if (n>=5)
            {
                for(int j=1;j<=n-i+1;j++){
                    cout<<" * ";
                }
            }
            
        }
        cout<<endl;
    }
}

