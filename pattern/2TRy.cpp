#include<iostream>
using namespace std;
int main(){
    int m;
    cout <<"Enter the value of n: ";
    cin>>m;
    for(int i=1;i<=m;i++){
        for(int j=1;j<=i;j++){
            if(m<=5){
                cout<<" * ";
            }
            else if (m>=5)
            {
                for(int j=1;j<=m-i+1;j++){
                    cout<<" * ";
                }
            }
            
        }
        cout<<endl;
    }
}

